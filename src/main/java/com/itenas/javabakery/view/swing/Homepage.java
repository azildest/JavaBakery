    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        
        String namaProduk, image;
        int idProduk;
        double harga;
        //keranjangService = new KeranjangServiceImpl();
        produkService = new ProdukServiceImpl();
        
        idProduk = Integer.parseInt(lblIDProduk.getText());
        String tempRef = String.valueOf(idProduk);
        namaProduk = txtNamaProduk.getText();
        harga = Double.parseDouble(txtHarga.getText());
        
        Produk produk = new Produk();
        produk.setId(idProduk);
        produk.setNama(namaProduk);
        produk.setHarga(harga);
        produk.setImage(produkImage);
        
        //Keranjang keranjang = new Keranjang();
        //keranjang.setIdproduk(idProduk);
        
        keranjangService = new KeranjangServiceImpl();
        List<Keranjang> listKeranjang;

        listKeranjang = keranjangService.findProductID(lblUsername.getText());
        Object[][] objectKeranjang = new Object[listKeranjang.size()][3];
        
        int counter = 0;
        
        for (Keranjang keranjang : listKeranjang) {
            objectKeranjang[counter][0] = keranjang.getIdproduk();
            String tempId = String.valueOf(objectKeranjang[counter][0]);
            
            if (tempRef.equals(tempId)){
                produkService.addAvailableProduct(lblUsername.getText(), produk);
                lblY.setText(tempRef);
                lblB.setText(tempId);
            } else {
                produkService.addProduct(lblUsername.getText(), produk);
                lblY.setText(tempRef);
                lblB.setText(tempId);
            }
            counter++;
        }
        
        //keranjangService.addProduct(lblUsername.getText(), keranjang);
        JOptionPane.showMessageDialog(null, "Produk telah ditambahkan ke Keranjang!");
        loadData();
        emptyField();
    }//GEN-LAST:event_btnAddToCartActionPerformed

