    private void loadData() {
        produkService = new ProdukServiceImpl();
        List<Produk> listProduk;
        sort = comboSortir.getSelectedItem().toString();
        
        if (sort.equals("Nama")){
            listProduk = produkService.sortByCondition3();
        } else if (sort.equals("Harga")){
            listProduk = produkService.sortByCondition4();
        } else {
            listProduk = produkService.findAllTersedia();
        }
        Object[][] objectProduk = new Object[listProduk.size()][6];
        
        int counter = 0;
        
        for (Produk produk : listProduk) {
            if (produk.getStatus().equals("Tersedia")){
                objectProduk[counter][0] = produk.getId();
                objectProduk[counter][1] = produk.getNama();
                objectProduk[counter][2] = produk.getHarga();
                objectProduk[counter][3] = produk.getImage();
            }
            counter++;
        }
        
        tblProduk.setModel(new javax.swing.table.DefaultTableModel(
            objectProduk,
            new String [] {
                "ID Produk", "Nama Produk", "Harga", "Image"
            }
        ));
    }
    
